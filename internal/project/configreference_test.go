const crefYAML = `prefix: example
vars:
  gateCmd: make gate
  checkCmd:
skills:
  - tdd
agents:
  - code-reviewer
`

func syncedProject(t *testing.T, configYAML string, files map[string]string) (string, *Project) {
	t.Helper()
	root := scaffoldFiles(t, configYAML, files)
	p, err := Open(root)
	if err != nil {
		t.Fatal(err)
	}
	if err := p.Sync(); err != nil {
		t.Fatal(err)
	}
	return root, p
}

// The generated reference renders per-project state: key rows with resolved
// defaults, the three-way var states, consumer/dormant hints, per-artifact
// data keys - and the document map cites it.
func TestConfigReferenceGolden(t *testing.T) {
	root, _ := syncedProject(t, crefYAML, nil)
	b, err := os.ReadFile(filepath.Join(root, "docs/config-reference.md"))
	if err != nil {
		t.Fatalf("config reference not written: %v", err)
	}
	got := string(b)
	for _, want := range []string{
		"GENERATED by awf",
		"# Configuration Reference",
		"`audit.diffThreshold` | int |",
		"| 400 (default) |",
		"`bootstrap.enabled`",
		"`currentState.maxTopicsPerPath` | positive int | 8 | 8 (default) |",
		"`currentState.maxClaimsPerTopic` | positive integer | 20 | 20 (default) |",
		"`currentState.topicCoverage` | severity (error, warn, or off) | error | error (default) |",
		"State: set (`make gate`). Consumed by: agents-doc, doc workflow, plans-template, skill tdd.",
		"`checkCmd`",
		"State: empty, an open to-do.",
		"State: absent, declined; the generic prose renders. Consumed by: agents-doc, doc workflow, skill tdd.", // testCmd
		"Dormant: no enabled artifact references it; enabling agent adr-reviewer, skill retrospective would.",   // invariantTestPath
		"`skill tdd` · `data.testSurfaces` (catalog default)",
		"`agent code-reviewer` · `data.correctnessTraps` (catalog default)",
		"`agents-doc` · `data.invariants`",
		"`sidecar.paths` (string list (anchored path globs))",
	} {
		if !strings.Contains(got, want) {
			t.Errorf("config reference missing %q:\n%s", want, got)
		}
	}
	if strings.Contains(got, "<no value>") {
		t.Error("config reference leaked <no value>")
	}
	agents, err := os.ReadFile(filepath.Join(root, "AGENTS.md"))
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(agents), "config-reference.md") {
		t.Error("AGENTS.md document map does not cite the config reference")
	}
}

