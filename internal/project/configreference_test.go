// Regeneration is the drift oracle: a hand-edit reports stale, a deletion
// missing, and a local: opt-out with a leftover lock entry orphaned.
// invariant: config/configuration:config-reference-regen-drift
func TestConfigReferenceRegenDrift(t *testing.T) {
	root, p := syncedProject(t, crefYAML, nil)
	rel := filepath.Join(root, "docs/config-reference.md")

	if err := os.WriteFile(rel, []byte("tampered"), 0o644); err != nil {
		t.Fatal(err)
	}
	assertDrift := func(kind, detail string) {
		t.Helper()
		drift, err := p.Check()
		if err != nil {
			t.Fatal(err)
		}
		for _, d := range drift {
			if d.Path == "docs/config-reference.md" && d.Kind == kind && strings.Contains(d.Detail, detail) {
				return
			}
		}
		t.Errorf("expected %s drift with %q for the config reference, got %v", kind, detail, drift)
	}
	assertDrift("stale", "out of date")

	if err := os.Remove(rel); err != nil {
		t.Fatal(err)
	}
	assertDrift("missing", "absent")

	// local: after a sync - generation stops, the lock entry reports orphaned.
	if err := os.WriteFile(filepath.Join(root, ".awf/config-reference.yaml"), []byte("local: true\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	p2, err := Open(root)
	if err != nil {
		t.Fatal(err)
	}
	drift, err := p2.Check()
	if err != nil {
		t.Fatal(err)
	}
	found := false
	for _, d := range drift {
		if d.Path == "docs/config-reference.md" && d.Kind == "orphaned" {
			found = true
		}
	}
	if !found {
		t.Errorf("local: config reference with a lock entry should report orphaned, got %v", drift)
	}
}

// The reference template consumes only dedicated data keys - a bare .vars or
// .data range would neutralize the consumption checks project-wide - and no
// partial references .vars, keeping the raw-source dormancy scan sound.
// invariant: config/configuration:config-reference-no-bare-vars
func TestConfigReferenceNoBareVars(t *testing.T) {
	_, p := syncedProject(t, crefYAML, nil)
	files, err := p.RenderAll()
	if err != nil {
		t.Fatal(err)
	}
	cref, ok, err := p.generateConfigReference(files)
	if err != nil {
		t.Fatal(err)
	}
	if !ok {
		t.Fatal("expected a generated reference")
	}
	if render.ReferencesBareVars(cref.assembled) || render.ReferencesBareData(cref.assembled) {
		t.Error("the reference template must not reference .vars or .data bare")
	}
	// Include-expansion guard: the dormancy scan reads raw template bytes, so
	// no partial may reference .vars, dotted or bare.
	entries, err := fs.ReadDir(templates.FS, "partials")
	if err != nil {
		t.Fatal(err)
	}
	for _, e := range entries {
		b, err := fs.ReadFile(templates.FS, "partials/"+e.Name())
		if err != nil {
			t.Fatal(err)
		}
		if render.ReferencesBareVars(string(b)) || len(render.ReferencedVars(string(b))) > 0 {
			t.Errorf("partial %s references .vars - the raw-source dormancy scan would go blind", e.Name())
		}
	}
}

// The config-reference sidecar is sections/local-only: data: and paths: refuse
// at open, unknown section names refuse, a declared-section drop renders.
// invariant: config/configuration:config-reference-data-rejected
func TestConfigReferenceSidecarRules(t *testing.T) {
	for _, tc := range []struct {
		name, sidecar, wantErr string
	}{
		{"data rejected", "data:\n  k: v\n", "data: has no effect"},
		{"paths rejected", "paths:\n  - '**/*.go'\n", "read only from domain sidecars"},
		{"unknown section rejected", "sections:\n  intr:\n    drop: true\n", "intr"},
	} {
		t.Run(tc.name, func(t *testing.T) {
			root := scaffoldFiles(t, crefYAML, map[string]string{"config-reference.yaml": tc.sidecar})
			if _, err := Open(root); err == nil || !strings.Contains(err.Error(), tc.wantErr) {
				t.Errorf("Open = %v, want error containing %q", err, tc.wantErr)
			}
		})
	}

	root, _ := syncedProject(t, crefYAML, map[string]string{
		"config-reference.yaml": "sections:\n  intro:\n    drop: true\n",
	})
	b, err := os.ReadFile(filepath.Join(root, "docs/config-reference.md"))
	if err != nil {
		t.Fatal(err)
	}
	if strings.Contains(string(b), "# Configuration Reference") {
		t.Error("dropped intro still rendered")
	}
	if !strings.Contains(string(b), "## config.yaml keys") {
		t.Error("generated tables must survive an intro drop")
	}
}

// Explicit audit values render without the default marker; explicit-empty
// lists render their accept-any/rule-off prose; a local-from-birth reference
// (never synced, no lock entry) reports nothing.
func TestConfigReferenceCurrentValues(t *testing.T) {
	auditYAML := crefYAML + `audit:
  subjectMaxLength: 80
  allowedTypes: []
  dependencyManifests: []
currentState:
  sources:
    - globs: ['**/*.md']
      marker: '#'
  testGlobs:
    - '**/*_test.md'
  topicCoverage: off
  topicFanout: error
  maxTopicsPerPath: 5
  maxClaimsPerTopic: 12
proseGate:
  enabled: true
  exemptions:
    - path: docs/x.md
      codepoint: U+2014
      count: 1
`
	root, _ := syncedProject(t, auditYAML, nil)
	b, err := os.ReadFile(filepath.Join(root, "docs/config-reference.md"))
	if err != nil {
		t.Fatal(err)
	}
	got := string(b)
	for _, want := range []string{
		"| 72 | 80 |", // configured, no "(default)" marker
		"| accept any |",
		"| rule off |",
		"| 1 entries |", // proseGate.exemptions live-state count
		"`currentState.sources` | list of {globs, marker, close} mappings | none | 1 sources |",
		"`currentState.testGlobs` | string list | none | 1 globs |",
		"`currentState.topicCoverage` | severity (error, warn, or off) | error | off |",
		"`currentState.topicFanout` | severity (error, warn, or off) | warn | error |",
		"`currentState.maxTopicsPerPath` | positive int | 8 | 5 |",
		"`currentState.maxClaimsPerTopic` | positive integer | 20 | 12 |",
	} {
		if !strings.Contains(got, want) {
			t.Errorf("configured audit values render wrong, missing %q", want)
		}
	}

	// local: from the very first sync - no lock entry, no drift, no file.
	root2, p2 := syncedProject(t, crefYAML, map[string]string{"config-reference.yaml": "local: true\n"})
	if _, err := os.Stat(filepath.Join(root2, "docs/config-reference.md")); err == nil {
		t.Error("local: config reference must not render")
	}
	drift, err := p2.Check()
	if err != nil {
		t.Fatal(err)
	}
	for _, d := range drift {
		if d.Path == "docs/config-reference.md" {
			t.Errorf("local-from-birth reference should report no drift, got %v", d)
		}
	}
}

// A part-read fault at the reference's intro (a directory where the part file
// may sit) surfaces from every generation call site - the reference renders
// outside RenderAll, so these branches are reachable, not theoretical.
func TestConfigReferencePartReadFault(t *testing.T) {
	root, p := syncedProject(t, crefYAML, nil)
	if err := os.MkdirAll(filepath.Join(root, ".awf/parts/config-reference/intro.md"), 0o755); err != nil {
		t.Fatal(err)
	}
	if _, err := p.Check(); err == nil {
		t.Error("Check should surface the part-read fault")
	}
	if err := p.Sync(); err == nil {
		t.Error("Sync should surface the part-read fault")
	}
	if _, err := p.AdvisoryNotes(); err == nil {
		t.Error("AdvisoryNotes should surface the part-read fault")
	}
	if _, err := p.PlannedOutputs(); err == nil {
		t.Error("PlannedOutputs should surface the part-read fault")
	}
}

